# f는 formatting의 약자이고 
# f-string은 문자열 안에 변수를 삽입할 수 있는 방법입니다.
# f-string은 문자열 앞에 f를 붙이고 중괄호 {} 안에 변수를 넣습니다
# f-string의 기능은 변수의 값을 문자열에 삽입하는 것입니다.
a = 12
print(f'The value of a is {a}')

b = 3.14
message = f'The value of b is {b}'
print(message)